    public NewJFrame() {
        initComponents();
             
        new Thread()
        {
            public void run()
            {
                while(true)
                {
                    Calendar cal = new GregorianCalendar(TimeZone.getTimeZone((String)time_zone.getSelectedItem()));
                    hour = cal.get(Calendar.HOUR);
                    minute = cal.get(Calendar.MINUTE);
                    second = cal.get(Calendar.SECOND);
                    AM_PM = cal.get(Calendar.AM_PM);
                    date = new Date();
                    
                    if(jRadioButton24.isSelected() && AM_PM == 1) //24 hour clock display is selected
                    {
                        hour += 12;
                    }
                    if(hour < 10)
                    {
                        time = "0" + hour + ":";
                    }
                    else
                    {
                        time = hour + ":";
                    }
                    
                    if(minute < 10)
                    {
                        time = time + "0" + minute + ":";
                    }
                    else
                    {
                        time = time + minute + ":";
                    }
                    
                    if(second < 10)
                    {
                        time = time + "0" + second + " ";
                    }
                    else
                    {
                        time = time + second + " ";
                    }
                    
                    if(jRadioButton12.isSelected()) //12 hour clock display is selected
                    {
                        switch(AM_PM)
                        {
                            case 0: time = time + "AM";
                                    break;
                            case 1: time = time + "PM";
                                    break;
                        }
                    }
                    
                    clock.setText(time);
                    date_field.setText(" " + date);
                }
            }            
        }.start();
    }

